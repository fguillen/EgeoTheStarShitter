namespace ABXY.Layers.Editor.Curve_Editor
{
    public struct Margin
    {
        public float left;
        public float right;
        public float top;
        public float bottom;

        public Margin(float left, float right, float top, float bottom)
        {
            this.left = left;
            this.right = right;
            this.top = top;
            this.bottom = bottom;
        }
    }
}
